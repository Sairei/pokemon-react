export const convertGameToColor = (gen) => {
  switch (gen) {
    case "red":
      return {
        text: '',
        background: 'red',
      };
    case "blue":
      return {
        text: '',
        background: 'blue',
      };
    case "generation-iii":
      return {
        text: '',
        background: '',
      };
    case "generation-iv":
      return {
        text: '',
        background: '',
      };
    case "generation-v":
      return {
        text: '',
        background: '',
      };
    case "generation-vi":
      return {
        text: '',
        background: '',
      };
    case "generation-vii":
      return {
        text: '',
        background: '',
      };
    case "generation-viii":
      return {
        text: '',
        background: '',
      };
    default:
      return {
        text: '',
        background: '',
      };
  }
}